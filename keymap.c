bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case BASE:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_BASE);
      }
      return false;
      break;
    case SYMB:
      if (record->event.pressed) {
        layer_on(_SYMB);
        update_tri_layer(_SYMB, _TMUX, _NUMBERS);
      } else {
        layer_off(_SYMB);
        update_tri_layer(_SYMB, _TMUX, _NUMBERS);
      }
      return false;
      break;
    case TMUX:
      if (record->event.pressed) {
        layer_on(_TMUX);
        update_tri_layer(_SYMB, _TMUX, _NUMBERS);
      } else {
        layer_off(_TMUX);
        update_tri_layer(_SYMB, _TMUX, _NUMBERS);
      }
      return false;
      break;
    case NUMBERS:
      if (record->event.pressed) {
        layer_on(_NUMBERS);
      } else {
        layer_off(_NUMBERS);
      }
      return false;
      break;
  }
  return true;
}
